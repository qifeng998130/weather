package com.ddn.why.domain.moji;

public class Day_Night {

	private String condition;// 天气
	private String img;// 图标
	private String moonphase;// 月相
	private String moonrise;// 月出
	private String moonset;// 月落
	private String temp;// 气温
	private String winddirect;// 风向
	private String windpower;// 风级
	private String windspeed;// 风速

	public String getCondition() {
		return condition;
	}

	public void setCondition(String condition) {
		this.condition = condition;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getMoonphase() {
		return moonphase;
	}

	public void setMoonphase(String moonphase) {
		this.moonphase = moonphase;
	}

	public String getMoonrise() {
		return moonrise;
	}

	public void setMoonrise(String moonrise) {
		this.moonrise = moonrise;
	}

	public String getMoonset() {
		return moonset;
	}

	public void setMoonset(String moonset) {
		this.moonset = moonset;
	}

	public String getTemp() {
		return temp;
	}

	public void setTemp(String temp) {
		this.temp = temp;
	}

	public String getWinddirect() {
		return winddirect;
	}

	public void setWinddirect(String winddirect) {
		this.winddirect = winddirect;
	}

	public String getWindpower() {
		return windpower;
	}

	public void setWindpower(String windpower) {
		this.windpower = windpower;
	}

	public String getWindspeed() {
		return windspeed;
	}

	public void setWindspeed(String windspeed) {
		this.windspeed = windspeed;
	}

}
